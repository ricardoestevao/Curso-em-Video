def fatorial(n): # Função que calcula o fatorial de um número
    f=1 # Inicializa o fatorial como 1
    for c in range(1, n+1): # Loop de 1 até n (inclusive)
        f*=c # Multiplica f pelo contador c
    return f # Retorna o fatorial calculado

